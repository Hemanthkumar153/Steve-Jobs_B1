package arithOperations;

public class AssignmentOperators {
	
	int addAssign(int a,int b) {
		return a+=b;
	}
	
	int subAssign(int a,int b) {
		return a-=b;
	}
	
	int mulAssign(int a,int b) {
		return a*=b;
	}
	
	int divAssign(int a,int b) {
		return a/=b;
	}
	
	int modAssign(int a,int b) {
		return a%=b;
	}
	
	
	
	
}
